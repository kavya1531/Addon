package session3;

public class loopingstatement {
	public static void main(String[] args) {
		int i=10;
		do
		{
			System.out.println("the value of i:"+i);
			i++;
		}
		while(i<=20);
			System.out.println("final value is :"+i);
		for(i=10;i<=20;i++)
		{
			System.out.println("the value is:"+i);
		}
	}

}
