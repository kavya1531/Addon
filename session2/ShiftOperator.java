package programs;

public class ShiftOperator {

public static void main(String[] args) {
	int a=10,b=5,c=2,d=4,e=20;
	
	// rightshift -->operand/2^num
	
	System.out.println("rightshift" +(a>>b));
	System.out.println("rightshift " +(a>>c));
	System.out.println("rightsift " +(e>>d));
	
	// left shift-->operand*2^num
	
	System.out.println("leftshift " +(a<<b));
	System.out.println("leftshift " +(a<<c));
	System.out.println("leftshift " +(e<<d));
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}








}
